    public void run() {
        try{
        while (RUN_TIMER) {
            tic++;
            if (tic == 100) {
                sec += 1;
                tic = 0;
            }
            if (sec == 60) {
                min += 1;
                sec = 0;
            }
            if (min == 60) {
                hour += 1;
                min = 0;
            }
            lblTic.setText(String.valueOf(tic));
            lblSec.setText(String.valueOf(sec));
            lblMin.setText(String.valueOf(min));
            lblHour.setText(String.valueOf(hour));
            Thread.sleep(10);
            
        }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
